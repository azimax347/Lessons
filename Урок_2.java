public class Урок_2 {
    public static void main(String[] args) {

        System.out.println("Hello world"); // Вывод текста с послед. переводом на новую строку (ln)
        System.out.print("Hello world \n"); // Перевод на следующую строку через "\n"
        System.out.print("test");

        int x = 10;
        double y = 35.3256723;
        System.out.printf("x = %d; y = %.3f", x, y); // Вывод через "printf"

    }
}
