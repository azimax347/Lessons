public class Урок_1 { // Имя файла
    public static void main(String[] args) {
        int x; // Объявление переменной
        x = 10; // Присвоение значения
        int y = 45; // Объявление + присвоение
        System.out.println(x); // Вывод 10
        System.out.println(y); // Вывод 45
        var z = 23.34; // Сам определяет тип переменной
        System.out.println(z);
        final var q = 93; // Объявление константы
        System.out.println(q);
        String h = "hello world";
        System.out.println(h);
    }
}